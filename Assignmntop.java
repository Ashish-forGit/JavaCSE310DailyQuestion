public class Assignmntop {
    public static void main(String[] args)
    {

    byte bt= 24;
    System.out.println("bt :"+ bt);

    bt +=10;
    System.out.println("bt :"+ bt);  // increment

    bt -=2;
    System.out.println("bt :"+ bt);  // decrement

    bt *=2;
    System.out.println("bt :"+ bt);

    bt /= 2;
    System.out.println("bt :"+ bt);

    bt %= 7;
    System.out.println("bt :"+ bt);
    }

}